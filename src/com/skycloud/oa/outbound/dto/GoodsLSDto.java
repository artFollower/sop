package com.skycloud.oa.outbound.dto;

public class GoodsLSDto {
	private String clientName ;
	private String type ;
	private long time ;
	private String ladingId ;
	private String changeCount ;
	private String currentCount ;
	private String saveCount ;
	private String deliverNo ;
	private String carshipNo ;
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public String getLadingId() {
		return ladingId;
	}
	public void setLadingId(String ladingId) {
		this.ladingId = ladingId;
	}
	public String getChangeCount() {
		return changeCount;
	}
	public void setChangeCount(String changeCount) {
		this.changeCount = changeCount;
	}
	public String getCurrentCount() {
		return currentCount;
	}
	public void setCurrentCount(String currentCount) {
		this.currentCount = currentCount;
	}
	public String getSaveCount() {
		return saveCount;
	}
	public void setSaveCount(String saveCount) {
		this.saveCount = saveCount;
	}
	public String getDeliverNo() {
		return deliverNo;
	}
	public void setDeliverNo(String deliverNo) {
		this.deliverNo = deliverNo;
	}
	public String getCarshipNo() {
		return carshipNo;
	}
	public void setCarshipNo(String carshipNo) {
		this.carshipNo = carshipNo;
	}
	
	
}
